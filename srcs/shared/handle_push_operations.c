#include "../../includes/checker.h"

void	handle_pa(int ***ab)
{
	int i;

	i = *(*ab)[2];
	if (*(*ab)[3] == 0)
		return ;
	while (i > 0)
	{
		(*ab)[0][i] = (*ab)[0][i - 1];
		i--;
	}
	(*ab)[0][0] = (*ab)[1][0];
	while (*(*ab)[3] > i)
	{
		(*ab)[1][i] = (*ab)[1][i + 1];
		i++;
	}
	(*(*ab)[2])++;
	(*(*ab)[3])--;
}

void	handle_pb(int ***ab)
{
	int i;

	i = *(*ab)[3];
	if (*(*ab)[2] == 0)
		return ;
	while (i > 0)
	{
		(*ab)[1][i] = (*ab)[1][i - 1];
		i--;
	}
	(*ab)[1][0] = (*ab)[0][0];
	while (*(*ab)[2] > i)
	{
		(*ab)[0][i] = (*ab)[0][i + 1];
		i++;
	}
	(*(*ab)[3])++;
	(*(*ab)[2])--;
}
