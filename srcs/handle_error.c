int		handle_error(void)
{
	write(2, "Error\n", 6);
	return (1);
}

int		check_error(int argc, char **argv)
{
	if (argc < 2)
		return (1);
	if (!check_array_int(argc, argv))
		return (1);
	return (0);
}
